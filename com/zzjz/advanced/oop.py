# 菱形继承
# MRO 就是多继承中用于保存继承顺序的一个列表
# python本身采用C3算法来计算多继承的菱形继承的计算
## MRO列表的原则
#    1、子类永远在父类前面
#    2、如果有多个父类，则根据继承语法中括号内的类的书写顺序存放
#    3、如果多个类继承了同一个父类，子孙类中只会选取继承语法括号中第一个父类的父类
